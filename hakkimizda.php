<th><td><img src="resim/motosikletcc.png" style="height:200px;"></img></td></th>	  
	 <th><td style="background-color: white; opacity: 0.7;">“Bizim amcaoğlu direk 600’lük aldı”, “arkadaşımın ilk motoru 1000’likti” gibi cümleler bir kriter oluşturmaz. Motosiklet kullanmak hele hele ilk defa kullanmak şakaya gelecek bir durum değildir. Motosikletlerin gaz tepkimeleri arabalara göre daha hassastır. Üstelik sadece iki tekerleği olan bir araçtan bahsediyoruz ki bu da durumu daha ciddiye almak gerektiğini gösteriyor. Peki kaç cc ile başlamak gerekir? Bu durum kişiden kişiye değişebilir. “Herkes 100cc ile başlamalı” gibi bir kural yada kaide yoktur. Kişinin bilinç durumu, yaşı, kendisini ve duygularını kontrol edebilmesi önemlidir. Yani kullandığı aracın gücünün farkında olmalı, bileğine hakim olmalıdır. “Ya bisiklet kullandım yıllarca, bunu da kullanırım” şeklinde düşünmeyin benzer yanları olsa da ikisi çok farklı araçlardır. Yeni başlayacaksanız ve kendinize öyle çok güvenmiyorsanız tavsiye olarak 50-150 cc arasında bir motosiklet almanızdır. Kendinize güveniyor ve ben bu işi yaparım diyorsanız ilk motosiklet olarak en fazla 250cc bir model almanızdır. İnanın o 600-1000 cc motosikletler ilk kullanım için oldukça agresif aletlerdir. Şöyle de düşünebilirsiniz hiç araba kullanmamış birine standart bir araba mı yoksa Ferrari mi tavsiye edersiniz? Eğer alacağınız motosiklet ikinci el ise dikkat etmeniz gereken bazı noktalar var.</td></th>
