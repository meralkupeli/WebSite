	  <h5>Motosiklet Ekipmanı Seçerken Nelere Dikkat Etmeliyiz?</h5>
	  Motosikletli bir yaşama adım atmanın en güvenilir yolu iyi bir sürüş eğitimi almak, kurallara uyum sağlayacak kadar disiplinli olmak ve 
	  elbette doğru ekipmanlara sahip olmaktan geçer. Motosiklet sadece sürücüsü ile var olan bir araç değildir. 
	  Aynı zamanda gerekli motor ekipmanları ile birliktegüvenilir bir şekilde hayat bulur. 
